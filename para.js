function textcolour(event){
    var a = event.target.value;
    document.getElementById("p1").style.color = a;
}

function textsize(event){
    var b = event.target.value;
    document.getElementById("p1").style.fontSize = b;
}

function textstyle(event){
    var c = event.target.value;
    document.getElementById("p1").style.fontStyle = c;
}